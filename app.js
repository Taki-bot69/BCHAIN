const contractAddress = '0xd97d497178ed5e7021f32478d4c35b3b849933ce'; 

let web3;
let contract;
let accounts;

const ac1 = async () => {
    if (typeof window.ethereum !== "undefined") {

        accounts = await ethereum.request({ method: "eth_requestAccounts" });
        document.getElementById("acc1").innerHTML = accounts[0];
        

        await loadContract(); 
    }
};

// Function to load the contract
async function loadContract() {
    if (typeof window.ethereum !== 'undefined') {
        web3 = new Web3(window.ethereum);
        contract = new web3.eth.Contract(abi, contractAddress);
    } else {
        alert('MetaMask not detected. Please install MetaMask.');
    }
}

// Vote for candidate
async function voteForCandidate(candidateIndex) {
    if (!contract) {
        console.error('Contract is not initialized');
        return; 
    }
    
    try {
        await contract.methods.voteForCandidate(candidateIndex).send({ from: accounts[0], gas: 300000 });

        // name
        const candidates = await contract.methods.getCandidates().call();
        alert(`You voted for candidate ${candidates[candidateIndex].name}`);

    } catch (error) {
        console.error('Voting failed', error);
        alert('You may have already voted for this Candidate.');
    }
}

